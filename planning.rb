  Possible Classes
  ******************************
  Player
  Game

  Roles
  ******************************
  The player class will create an instance of each player. It will have an initialize method and have instance
  variable to represent the number of lives. There will be a name instance variable that sets the players name.
  
  The game class will be an instance of a game. This class will contain the game loop. 
  Game will have a current_player instance variable. The game class will have a play method that starts
  the game loop. It will also have a private ask_question method which generates the question and a switch_player private
  method that switches the current player.The initialize method in the Game class
  creates 2 instances of the Player class (player1, player2). 
  
